    private void getAllDbRequiredInfo(String product_id, int user_seller, int amount) throws SQLException {
        ResultSet data_from_db = querydb.getSingleProductDb( product_id, user_seller);
        while (data_from_db.next()){
            product_from_db_name = data_from_db.getString(2);
            amount_from_db = data_from_db.getString(3);
            product_from_db_price = data_from_db.getString(4);
        }

        amount_to_sell = Integer.parseInt(amount_from_db) - amount;
        total_sell_price_with_no_iva = amount_to_sell * Float.parseFloat(product_from_db_price);
        iva_price = (IVA * total_sell_price_with_no_iva);
        price_with_iva = iva_price + total_sell_price_with_no_iva;
    }

