    private void create_invoiceActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, DocumentException, FileNotFoundException {//GEN-FIRST:event_create_invoiceActionPerformed
        int isUpdated = 0;
        String invoiceName = generateNamePath.generatePathToSaveInfo("Factura", desktopPath);
        Document document = invoice.generateSingleInvoice(invoiceName);

        if(customer_full_name.getText().isEmpty() || customer_email.getText().isEmpty() || customer_number.getText().isEmpty() || customer_address.getText().isEmpty() || customer_postal_number.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios para continuar");
            return;
        }else if( !validations.validateEmailAddress(customer_email.getText())){
            JOptionPane.showMessageDialog(null, "Por favor ingresa un correo valido");
            return;
        }else {
            isUpdated = querydb.updateRecordOnDb(amount_to_extract, user_id, product_id);
        }
        //? Validar que si se haya actualizado para crear la factura
        if(isUpdated == 1){
            //? Llenar los datos
            invoice.createHeader(document);
            invoice.billingCustomerInfo(document, customer_full_name.getText(), customer_number.getText(), customer_email.getText(), customer_address.getText(), customer_postal_number.getText());
            invoice.productInfoTable(document, product_from_db_name, amount_to_sell, total_sell_price_with_no_iva, IVA, iva_price);
            invoice.tableTotalProduct(document, price_with_iva);
            invoice.footerContent(document);
            document.newPage();
            document.close();
            JOptionPane.showMessageDialog(null, "Proceso realizado correctamente : )");
//            this.setVisible(false);
        }else {
            JOptionPane.showMessageDialog(null, "Oops, algo no salio como lo esperabamos :(");
        }
        this.dispose();
    }//GEN-LAST:event_create_invo iceActionPerformed

