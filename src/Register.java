    private void register_accountActionPerformed(java.awt.event.ActionEvent evt) throws SQLException {//GEN-FIRST:event_register_accountActionPerformed
        ResultSet singleAccountFound = null;
        String email_user_found = "";
        //? Registrar aca datos
        String userName = name_user.getText();
        String lastName = last_name.getText();
        String email = email_user.getText();
        char[] password = password_user.getPassword();

        if(validations.validateRegisterFields(userName, lastName, email, new String(password))){
            JOptionPane.showMessageDialog(null, "Los campos no pueden estar vacios", "Opps", JOptionPane.WARNING_MESSAGE);
        }else {
            //? Validar correo
            if(!validations.validateEmailAddress(email)){
                JOptionPane.showMessageDialog(null, "Opps, el correo que ingresaste no parece ser valido", "Correo invalido", JOptionPane.WARNING_MESSAGE);
                return;
            }
            //? Validar password
            if(validations.validatePasswordLength(new String(password))){
                JOptionPane.showMessageDialog(null, "La contrase√±a debe ser mayor a 6 caracteres", "Contrase√±a Invalida", JOptionPane.WARNING_MESSAGE);
                return;
            }

            try {
                singleAccountFound = dbQuery.findSingleAccount(email);
            }catch (SQLException e){
                System.out.println(e+"algo salio mal");
            }

            System.out.println(singleAccountFound);
            if(singleAccountFound != null){
                while(singleAccountFound.next()){
                    email_user_found = singleAccountFound.getString(4);
                }
            }

            if(email_user_found.length() > 1){
                JOptionPane.showMessageDialog(null, "Por favor selecciona otro correo", "Correo no valido", JOptionPane.WARNING_MESSAGE);
                return;
            }

            System.out.println(email_user_found+"encontro algo");
            int user_added = dbQuery.insertNewUser(userName, lastName, email, new String(password));
            if(user_added == 1){
                JOptionPane.showMessageDialog(null, "Tu cuenta ha sido creada con exito", "Exito", JOptionPane.INFORMATION_MESSAGE);
                Login login = new Login();
                login.show();
                dispose();
            }else {
                JOptionPane.showMessageDialog(null, "Oops algo salio mal, intenta de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_register_accountActionPerformed

